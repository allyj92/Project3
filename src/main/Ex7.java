package main;

public class Ex7 {

	public static void main(String[] args) {
		int x = 10;

		x += 10; // x =x+10 ==> 20
		System.out.println(x);
		x -= 5; // x = x-5 ==> 15
		System.out.println(x);
		x *= 10; // x= x*10 ==> 150
		System.out.println(x);
		x /= 10;// x = x/10 ==> 15
		System.out.println(x);
		x %= 10; // x = x%10 ==> 5
		System.out.println(x);
	}

}
